///
/// \file TaskView.h
/// \brief Заголовочный файл класса для взаимодействия с интерфейсом задач
///
#ifndef TASK_VIEW_H
#define TASK_VIEW_H

#include <string>
#include <iostream>

 ///
 /// \brief Класс для работы с интерфейсом управления задачами
 /// \details Отвечает за:
 /// - Отображение меню администратора
 /// - Выбор задач
 /// - Визуализацию статистики пользователей
 ///
class TaskView {
public:
    ///
    /// \brief Отображает основной экран задач
    /// \details Выводит заголовок "=== Task Screen ==="
    ///
    void display();
    ///
    /// \brief Показывает меню администратора
    /// \details Доступные пункты:
    /// 1. Просмотр задач
    /// 2. Добавление задачи
    /// 3. Удаление задачи
    /// 4. Просмотр статистики пользователей
    ////
    void showAdminMenu();
    ///
    /// \brief Запрашивает выбор задачи у пользователя
    /// \return Название выбранной задачи
    /// \note Использует стандартный ввод (std::cin)
    ///
    std::string selectTask();
    ///
    /// \brief Отображает статистику пользователей
    /// \param stats Строка с данными статистики
    /// \details Форматирует вывод в виде:
    /// \code
    /// === User Statistics ===
    /// [данные]
    /// \endcode
    ///
    void showUserStats(const std::string& stats); // Новый метод
};

#endif // TASK_VIEW_H
